    public FollowingsFrame() {
        initComponents();
        Integer FollowingNumber = 0;

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection connect = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/socialmedia?user=root");
            String sql = "SELECT * FROM follow WHERE username1=?";
            PreparedStatement ps = connect.prepareStatement(sql);
            ps.setString(1, LogInFrame.UN);
            ResultSet rs = ps.executeQuery();

            while (table.getRowCount() > 0) {
                ((DefaultTableModel) table.getModel()).removeRow(0);
            }

            while (rs.next()) {
                FollowingNumber++;
                lbl_Followings.setText(FollowingNumber.toString());
                ((DefaultTableModel) table.getModel()).addRow(new Object[]{rs.getString("username2")});
            }
            
            connect.close();
            ps.close();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            e.printStackTrace();
        }

    }

